    private void addTableColumn(){
        tableModel.addColumn("Course Code");
        tableModel.addColumn("Course Name");
        
        courseTable.setModel(tableModel);
    }
    private void addTableRowData(){
        tableModel.setRowCount(0);
        CourseDao dao= new CourseDao();
        List<Course> courses = dao.allCourses();
        for(Course theCourse : courses){
            tableModel.addRow(new Object[]{
                theCourse.getCourseCode(),
                theCourse.getCourseName(),
            });
        }
    }


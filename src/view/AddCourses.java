    private void addCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseBtnActionPerformed
        if(courseCodeTxt.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Course Code is Mandatory","Mandatory Field",JOptionPane.WARNING_MESSAGE);
        }else if(courseNameTxt.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Course Name is Mandatory","Mandatory Field",JOptionPane.WARNING_MESSAGE);
        }
        
        Course theCourse = new Course();
        theCourse.setCourseCode(courseCodeTxt.getText());
        theCourse.setCourseName(courseNameTxt.getText());
        
        CourseDao dao = new CourseDao();
        String feedback = dao.addCourse(theCourse);
        JOptionPane.showMessageDialog(this, feedback);
        
        courseCodeTxt.setText(""); 
        courseNameTxt.setText("");
        addTableRowData();
    }//GEN-LAST:event_addCourseBtnActionPerformed


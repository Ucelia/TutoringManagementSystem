    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        if(courseCodeSearchTxt.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Search Course Code is Mandatory");
        }else{
            Course theCourse = new Course();
            theCourse.setCourseCode(courseCodeSearchTxt.getText());
            CourseDao dao = new CourseDao();
            Course courseObj = dao.searchCourse(theCourse);
            if(courseObj != null){
                courseCodeTxt.setText(courseObj.getCourseCode());
                courseNameTxt.setText(courseObj.getCourseName());
            }else{
                JOptionPane.showMessageDialog(this, "Course doesn't exist");
            }
        }
        addTableRowData();
        //courseCodeSearchTxt.setText("");
        
        
    }//GEN-LAST:event_searchBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        if(courseCodeTxt.getText().trim().isEmpty()|| courseNameTxt.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "all Fields are required");
       }else{
           Course theCourse = new Course();
           theCourse.setCourseCode(courseCodeTxt.getText());
           theCourse.setCourseName(courseNameTxt.getText());
           CourseDao dao = new CourseDao();
           String feedback = dao.updateCourse(theCourse);
           JOptionPane.showMessageDialog(this, feedback);
       }
        courseCodeTxt.setText(""); 
        courseNameTxt.setText("");
        addTableRowData();
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int response = JOptionPane.showConfirmDialog(null, "Do you really want to delete seleted record", "Confirm", JOptionPane.YES_NO_OPTION);
       if(response==JOptionPane.YES_OPTION){
        
        if(courseCodeTxt.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Student Id is mandatory");
        }else{
            Course theCourse = new Course();
            theCourse.setCourseCode(courseCodeTxt.getText());
            CourseDao dao = new CourseDao();
            String feedback = dao.deleteCourse(theCourse);
            JOptionPane.showMessageDialog(this, feedback);
        }}else{
           JOptionPane.showMessageDialog(this, "Course not deleted");
       }
       courseCodeTxt.setText(""); 
        courseNameTxt.setText("");
        addTableRowData();
    }//GEN-LAST:event_deleteBtnActionPerformed

